#include "MyMath.h"


float MyMath::Add(float a, float b)
{
	return a + b;
}

float MyMath::Sub(float a, float b)
{
	return a - b;
}

float MyMath::Mlt(float a, float b)
{
	return a * b;
}

float MyMath::Div(float a, float b)
{
	return a / b;
}

float MyMath::Pow(float a, float b)
{
	return a ^ b;
}